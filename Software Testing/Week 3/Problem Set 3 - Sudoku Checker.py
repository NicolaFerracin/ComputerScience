def check_sudoku(grid):
    ###Your code here.
    # check length of rows and column
    if (len(grid) != 9):
        return None

    for i in range(len(grid)):
        if len(grid[i]) != 9:
            return None
        # check sequence by rows
        if checkArrayForDoubles(grid[i]) == False:
            return False
        # check sequence by cols
        if checkArrayForDoubles([grid[0][i], grid[1][i], grid[2][i], grid[3][i], grid[4][i], grid[5][i], grid[6][i], grid[7][i], grid[8][i]]) == False:
            return False

    if checkSubGrids(grid) == False:
        return False
    return True

def checkSubGrids(grid):
    for i in range(3):
        for j in range(3):
            if (checkArrayForDoubles([grid[i*3][j*3],
                                  grid[i*3][j*3 + 1],
                                  grid[i*3][j*3 + 2],
                                  grid[i*3 + 1][j*3],
                                  grid[i*3 + 1][j*3 + 1],
                                  grid[i*3 + 1][j*3 + 2],
                                  grid[i*3 + 2][j*3],
                                  grid[i*3 + 2][j*3 + 1],
                                  grid[i*3 + 2][j*3 + 2]]) == False):
                return False
    return True

def checkArrayForDoubles(arr):
    arr.reverse()
    rev = arr[:]
    arr.reverse()
    for i in range(1, 9):
        if i in arr:
            if len(arr) - arr.index(i) - 1 != rev.index(i):
                return False
    return True


